# Program to compute the area and perimeter of the square

# Taking the side as input from the user
Side = int(input("Enter the value of side of sqaure : "))

Area = Side * Side          # Computing Area of Sqaure
Perimeter = 4 * Side        # Computing Perimeter of Sqaure

# printing statements
print("The Area of sqaure = ", Area)
print("The Perimeter of sqaure = ", Perimeter)
