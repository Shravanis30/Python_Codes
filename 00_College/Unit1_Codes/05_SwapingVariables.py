# Program to swap the values of two variable

# Taking the value of 2 variables from the user
A = input("Enter the value of A : ")
B = input("Enter the value of B : ")

# Printing the values before swapping
print()
print("The value of A before swapping = ", A)
print("The value of B before swapping = ", B)
print()

# Computing the swapping function
C = A
A = B
B = C

# Printing the values after swapping
print("The value of A after swapping = ", A)
print("The value of B after swapping = ", B)